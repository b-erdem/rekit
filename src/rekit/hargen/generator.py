def generate_client(
    spec: ApiSpec,
    output_dir: str | Path,
    client_name: str = "ApiClient",
    package_name: str = "api_client",
) -> Path:
    """
    Generate a typed Python API client from an ApiSpec.

    Creates an output directory with:
      - __init__.py (package marker)
      - models.py (dataclass models)
      - client.py (client class)

    Args:
        spec: The analyzed API specification.
        output_dir: Directory to write generated files into.
        client_name: Name for the client class (e.g., "IdealIstaClient").
        package_name: Python package name for imports within generated code.

    Returns:
        Path to the output directory.
    """
    output = Path(output_dir)
    output.mkdir(parents=True, exist_ok=True)

    # Collect all models needed
    models, model_names_set = _collect_models(spec, client_name)

    # Build endpoint definitions
    endpoints = _build_endpoint_defs(spec, model_names_set)

    # Classify headers
    static_headers = [h for h in spec.common_headers if h.classification == "static"]
    dynamic_headers = [h for h in spec.common_headers if h.classification == "dynamic"]
    auth_headers = spec.auth_headers

    # All model names for imports in client.py
    model_imports = sorted(m.class_name for m in models)

    # Render templates
    env = Environment(
        loader=BaseLoader(),
        keep_trailing_newline=True,
        trim_blocks=True,
        lstrip_blocks=True,
    )
    env.globals["repr"] = repr

    models_code = env.from_string(MODELS_TEMPLATE).render(
        client_name=client_name,
        models=models,
    )

    client_code = env.from_string(CLIENT_TEMPLATE).render(
        client_name=client_name,
        package_name=package_name,
        base_url=spec.base_url,
        static_headers=static_headers,
        dynamic_headers=dynamic_headers,
        auth_headers=auth_headers,
        model_imports=model_imports,
        endpoints=endpoints,
    )

    # Write files
    init_path = output / "__init__.py"
    models_path = output / "models.py"
    client_path = output / "client.py"

    init_path.write_text(
        f'"""\nAuto-generated API client package: {package_name}\n"""\n',
        encoding="utf-8",
    )
    models_path.write_text(models_code, encoding="utf-8")
    client_path.write_text(client_code, encoding="utf-8")

    console.print(f"[green]Generated client:[/green] {output}")
    console.print(f"  [dim]{init_path}[/dim]")
    console.print(f"  [dim]{models_path}[/dim]")
    console.print(f"  [dim]{client_path}[/dim]")
    console.print(f"  [dim]{len(models)} models, {len(endpoints)} endpoints[/dim]")

    return output


