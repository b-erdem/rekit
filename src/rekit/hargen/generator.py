def _sanitize_name(name: str) -> str:
    """Sanitize a string to be a valid Python identifier."""
    # Replace non-alphanumeric chars with underscores
    clean = re.sub(r"[^a-zA-Z0-9_]", "_", name)
    # Remove leading digits
    clean = re.sub(r"^(\d)", r"_\1", clean)
    # Deduplicate underscores
    clean = re.sub(r"_+", "_", clean).strip("_")
    # Avoid Python keywords
    if keyword.iskeyword(clean):
        clean = clean + "_"
    return clean or "field_"


def _to_class_name(name: str) -> str:
    """Convert a string to PascalCase class name."""
    parts = re.split(r"[_\-\s]+", name)
    return "".join(p.capitalize() for p in parts if p)


def _collect_models(
    spec: ApiSpec, client_name: str
) -> Tuple[List[_ModelDef], Set[str]]:
    """
    Collect all model definitions needed from the ApiSpec.

    Returns (list of _ModelDef, set of model class names).
    """
    models: List[_ModelDef] = []
    names_used: Set[str] = set()

    def _unique_name(base: str) -> str:
        name = base
        counter = 2
        while name in names_used:
            name = f"{base}{counter}"
            counter += 1
        names_used.add(name)
        return name

    def _schema_to_model(
        schema: List[FieldSchema],
        class_name: str,
        docstring: str,
    ) -> Optional[_ModelDef]:
        """Recursively convert a FieldSchema list to a _ModelDef."""
        if not schema:
            return None

        # Skip pseudo-schemas (single _items or _value)
        if len(schema) == 1 and schema[0].name in ("_items", "_value"):
            # For list responses, we process the nested items
            fs = schema[0]
            if fs.nested:
                return _schema_to_model(fs.nested, class_name, docstring)
            return None

        fields: List[_ModelField] = []
        for fs in schema:
            if fs.name.startswith("_"):
                continue

            attr_name = _sanitize_name(fs.name)
            key_repr = repr(fs.name)

            nested_model = ""
            nested_from_dict = ""
            is_list = False

            # Check for nested object fields
            if fs.nested and fs.type_str.startswith("List["):
                # List of nested objects
                inner_name = _unique_name(_to_class_name(fs.name) + "Item")
                inner_model = _schema_to_model(
                    fs.nested, inner_name, f"Item in {fs.name}"
                )
                if inner_model:
                    models.append(inner_model)
                    nested_model = inner_name
                    is_list = True
                    type_annotation = f"List[{inner_name}]"
                    nested_from_dict = f"[{inner_name}.from_dict(item) for item in data.get({key_repr}, [])]"
                else:
                    type_annotation = _clean_type(fs.type_str, fs.optional)
            elif fs.nested and (
                "Dict[" in fs.type_str or "Optional[Dict[" in fs.type_str
            ):
                # Nested object
                inner_name = _unique_name(_to_class_name(fs.name))
                inner_model = _schema_to_model(
                    fs.nested, inner_name, f"Schema for {fs.name}"
                )
                if inner_model:
                    models.append(inner_model)
                    nested_model = inner_name
                    if fs.optional:
                        type_annotation = f"Optional[{inner_name}]"
                        nested_from_dict = (
                            f"{inner_name}.from_dict(data[{key_repr}]) "
                            f"if {key_repr} in data and data[{key_repr}] is not None "
                            f"else None"
                        )
                    else:
                        type_annotation = inner_name
                        nested_from_dict = (
                            f"{inner_name}.from_dict(data.get({key_repr}, {{}}))"
                        )
                else:
                    type_annotation = _clean_type(fs.type_str, fs.optional)
            else:
                type_annotation = _clean_type(fs.type_str, fs.optional)

            # Determine default value
            if fs.optional:
                default = " = None"
                default_value_repr = "None"
            elif "List[" in type_annotation and not nested_model:
                default = " = field(default_factory=list)"
                default_value_repr = "[]"
            else:
                default = ""
                default_value_repr = "None"

            fields.append(
                _ModelField(
                    attr_name=attr_name,
                    key_repr=key_repr,
                    type_annotation=type_annotation,
                    default=default,
                    default_value_repr=default_value_repr,
                    nested_model=nested_model,
                    nested_from_dict=nested_from_dict,
                    is_list=is_list,
                    example=fs.example_value,
                )
            )

        # Sort: required fields first (those without defaults), then optional
        fields.sort(key=lambda f: (bool(f.default), f.attr_name))

        return _ModelDef(
            class_name=class_name,
            docstring=docstring,
            fields=fields,
        )

    # Process each endpoint
    for ep in spec.endpoints:
        # Response model
        if ep.response_schema:
            resp_name = _unique_name(ep.response_model_name)
            resp_model = _schema_to_model(
                ep.response_schema,
                resp_name,
                f"Response model for {ep.method} {ep.path_pattern}",
            )
            if resp_model:
                models.append(resp_model)

        # Request model
        if ep.request_schema:
            req_name = _unique_name(ep.request_model_name)
            req_model = _schema_to_model(
                ep.request_schema,
                req_name,
                f"Request model for {ep.method} {ep.path_pattern}",
            )
            if req_model:
                models.append(req_model)

    return models, names_used


def _clean_type(type_str: str, optional: bool = False) -> str:
    """Clean up a type string for use in generated code."""
    # Ensure Optional wrapping is correct
    if optional and not type_str.startswith("Optional["):
        return f"Optional[{type_str}]"
    return type_str


def _build_endpoint_defs(spec: ApiSpec, model_names: Set[str]) -> List[_EndpointDef]:
    """Build template context for each endpoint method."""
    endpoints: List[_EndpointDef] = []
    func_names_used: Set[str] = set()

    for ep in spec.endpoints:
        func_name = ep.function_name
        # Ensure unique function names
        counter = 2
        base_name = func_name
        while func_name in func_names_used:
            func_name = f"{base_name}_{counter}"
            counter += 1
        func_names_used.add(func_name)

        # Path params
        path_params = [
            {"name": _sanitize_name(p.name), "type_str": p.type_str}
            for p in ep.path_params
        ]

        # Build f-string for path with params
        path_format = ep.path_pattern
        for p in ep.path_params:
            safe_name = _sanitize_name(p.name)
            path_format = path_format.replace(f"{{{p.name}}}", f"{{{safe_name}}}")

        # Request model
        request_model = ""
        if ep.request_schema and ep.method in ("POST", "PUT", "PATCH"):
            candidate = ep.request_model_name
            if candidate in model_names:
                request_model = candidate

        # Response model
        response_model = ""
        item_model = ""
        returns_model = False
        returns_list = False
        return_type = "Any"

        if ep.response_schema:
            candidate = ep.response_model_name
            if candidate in model_names:
                # Check if response is a list
                if (
                    len(ep.response_schema) == 1
                    and ep.response_schema[0].name == "_items"
                    and ep.response_schema[0].nested
                ):
                    # List response
                    item_name = _to_class_name(ep.response_schema[0].name) + "Item"
                    # Find the actual model name (may have been renamed)
                    for mn in model_names:
                        if mn.startswith(item_name) or mn == candidate:
                            item_model = mn
                            break
                    if item_model:
                        returns_list = True
                        returns_model = True
                        return_type = f"List[{item_model}]"
                    else:
                        response_model = candidate
                        returns_model = True
                        return_type = candidate
                else:
                    response_model = candidate
                    returns_model = True
                    return_type = candidate
            else:
                return_type = "Any"

        # Query params
        required_qp = []
        optional_qp = []
        all_qp = []
        for qp in ep.query_params:
            safe_name = _sanitize_name(qp.name)
            entry = {
                "name": safe_name,
                "original_name": qp.name,
                "type_str": qp.type_str,
                "required": qp.required,
            }
            all_qp.append(entry)
            if qp.required:
                required_qp.append(entry)
            else:
                optional_qp.append(entry)

        endpoints.append(
            _EndpointDef(
                func_name=func_name,
                method=ep.method,
                path_pattern=ep.path_pattern,
                path_format_str=path_format,
                path_params=path_params,
                request_model=request_model,
                response_model=response_model,
                item_model=item_model,
                return_type=return_type,
                returns_model=returns_model,
                returns_list=returns_list,
                required_query_params=required_qp,
                optional_query_params=optional_qp,
                all_query_params=all_qp,
                status_codes=ep.status_codes,
                request_count=ep.request_count,
            )
        )

    return endpoints
