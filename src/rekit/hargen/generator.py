"""
hargen.generator — Generate typed Python API client code from an ApiSpec.

Produces two files:
  - models.py: Dataclass models for request/response bodies
  - client.py: Client class with typed methods for each endpoint

Uses Jinja2 templates (inline) for clean, maintainable output.
"""

from __future__ import annotations

import keyword
import logging
import re
import textwrap
from dataclasses import dataclass
from pathlib import Path
from typing import Any, Dict, List, Optional, Set, Tuple

from jinja2 import Environment, BaseLoader
from rich.console import Console

from rekit.hargen.analyzer import ApiSpec, FieldSchema

logger = logging.getLogger(__name__)
console = Console(stderr=True)

# ---------------------------------------------------------------------------
# Jinja2 templates
# ---------------------------------------------------------------------------

MODELS_TEMPLATE = textwrap.dedent('''\
    """
    Auto-generated API models for {{ client_name }}.

    Generated by rekit hargen from captured HTTP traffic.
    Do not edit directly — regenerate from updated traffic captures.
    """

    from __future__ import annotations

    from dataclasses import dataclass, field
    from typing import Any, Dict, List, Optional


    {% for model in models %}
    @dataclass
    class {{ model.class_name }}:
        """{{ model.docstring }}"""
    {% for f in model.fields %}
        {{ f.attr_name }}: {{ f.type_annotation }}{{ f.default }}
    {% endfor %}
    {% if not model.fields %}
        pass
    {% endif %}

        @classmethod
        def from_dict(cls, data: Dict[str, Any]) -> "{{ model.class_name }}":
            """Create an instance from a dictionary."""
            if data is None:
                data = {}
            return cls(
    {% for f in model.fields %}
    {% if f.nested_model %}
                {{ f.attr_name }}={{ f.nested_from_dict }},
    {% else %}
                {{ f.attr_name }}=data.get({{ f.key_repr }}, {{ f.default_value_repr }}),
    {% endif %}
    {% endfor %}
            )

        def to_dict(self) -> Dict[str, Any]:
            """Convert to a dictionary for serialization."""
            result: Dict[str, Any] = {}
    {% for f in model.fields %}
    {% if f.nested_model %}
    {% if f.is_list %}
            result[{{ f.key_repr }}] = [item.to_dict() for item in self.{{ f.attr_name }}] if self.{{ f.attr_name }} else []
    {% else %}
            result[{{ f.key_repr }}] = self.{{ f.attr_name }}.to_dict() if self.{{ f.attr_name }} else None
    {% endif %}
    {% else %}
            result[{{ f.key_repr }}] = self.{{ f.attr_name }}
    {% endif %}
    {% endfor %}
            return result

    {% endfor %}
''')

