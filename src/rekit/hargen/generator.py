@dataclass
class _ModelField:
    """Template context for a single model field."""

    attr_name: str
    key_repr: str  # repr of the original dict key
    type_annotation: str
    default: str  # e.g., " = None" or " = field(default_factory=list)"
    default_value_repr: str  # Python repr for from_dict default
    nested_model: str  # Empty string if not nested
    nested_from_dict: str  # Expression for from_dict with nested model
    is_list: bool
    example: Any


@dataclass
class _ModelDef:
    """Template context for a model class."""

    class_name: str
    docstring: str
    fields: List[_ModelField]


@dataclass
class _EndpointDef:
    """Template context for a client method."""

    func_name: str
    method: str
    path_pattern: str
    path_format_str: str  # Python f-string path e.g., /api/listings/{listing_id}
    path_params: List[Dict[str, str]]
    request_model: str
    response_model: str
    item_model: str  # For list responses
    return_type: str
    returns_model: bool
    returns_list: bool
    required_query_params: List[Dict[str, str]]
    optional_query_params: List[Dict[str, str]]
    all_query_params: List[Dict[str, Any]]
    status_codes: List[int]
    request_count: int


