def _generate_hmac_class(flow: AuthFlow, class_name: str) -> str:
    """Generate HMAC signature auth class."""
    sig_header = "X-Signature"
    if flow.tokens_involved:
        sig_header = next(iter(flow.tokens_involved))

    return textwrap.dedent(f'''\

    class {class_name}:
        """HMAC Signature authentication handler."""

        SIGNATURE_HEADER = "{sig_header}"

        def __init__(self, api_key: str, secret_key: str):
            self.api_key = api_key
            self.secret_key = secret_key
            self.session = requests.Session()

        def _sign_request(self, method: str, url: str, body: str = "") -> str:
            """Generate HMAC signature for a request."""
            import hashlib
            import hmac as hmac_mod

            timestamp = str(int(time.time()))
            message = f"{{method}}\\n{{url}}\\n{{timestamp}}\\n{{body}}"
            signature = hmac_mod.new(
                self.secret_key.encode(),
                message.encode(),
                hashlib.sha256,
            ).hexdigest()
            return signature

        def prepare_request(
            self, method: str, url: str, headers: Optional[Dict[str, str]] = None
        ) -> Tuple[str, str, Dict[str, str]]:
            """Add HMAC signature to a request."""
            import time as time_mod

            headers = dict(headers) if headers else {{}}
            signature = self._sign_request(method, url)
            headers[self.SIGNATURE_HEADER] = signature
            headers["X-Timestamp"] = str(int(time_mod.time()))
            return method, url, headers
    ''')


def _generate_stub_class(class_name: str) -> str:
    """Generate a stub auth class when no specific flow is detected."""
    return textwrap.dedent(f'''\

    class {class_name}:
        """Authentication handler (no specific flow detected)."""

        def __init__(self):
            self.session = requests.Session()

        def prepare_request(
            self, method: str, url: str, headers: Optional[Dict[str, str]] = None
        ) -> Tuple[str, str, Dict[str, str]]:
            """Prepare a request (no auth added)."""
            headers = dict(headers) if headers else {{}}
            return method, url, headers
    ''')
